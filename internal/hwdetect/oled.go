// SSD1306/SH1106 I2C command and data prefixes.
const (
	oledCmdPrefix  = 0x00 // Co=0, D/C#=0 → command
	oledDataPrefix = 0x40 // Co=0, D/C#=1 → data
)

// oledSendCmd sends one or more command bytes to the OLED controller.
func oledSendCmd(fd int, cmds ...byte) {
	for _, c := range cmds {
		unix.Write(fd, []byte{oledCmdPrefix, c})
	}
}

// oledSendData sends a slice of data bytes (pixel data) to the OLED controller.
// I2C writes are chunked to stay within typical driver limits.
func oledSendData(fd int, data []byte) {
	const chunk = 128
	for len(data) > 0 {
		n := chunk
		if n > len(data) {
			n = len(data)
		}
		buf := make([]byte, 1+n)
		buf[0] = oledDataPrefix
		copy(buf[1:], data[:n])
		unix.Write(fd, buf)
		data = data[n:]
	}
}

// oledInitSSD1306 initialises an SSD1306 128x64 OLED display.
func oledInitSSD1306(fd int) {
	oledSendCmd(fd, 0xAE)       // display off
	oledSendCmd(fd, 0xD5, 0x80) // clock divider
	oledSendCmd(fd, 0xA8, 0x3F) // MUX ratio = 64
	oledSendCmd(fd, 0xD3, 0x00) // display offset = 0
	oledSendCmd(fd, 0x40)       // start line = 0
	oledSendCmd(fd, 0x8D, 0x14) // charge pump on
	oledSendCmd(fd, 0x20, 0x00) // horizontal addressing mode
	oledSendCmd(fd, 0xA1)       // segment remap (flip horizontal)
	oledSendCmd(fd, 0xC8)       // COM scan direction (flip vertical)
	oledSendCmd(fd, 0xDA, 0x12) // COM pins config
	oledSendCmd(fd, 0x81, 0xCF) // contrast
	oledSendCmd(fd, 0xD9, 0xF1) // pre-charge period
	oledSendCmd(fd, 0xDB, 0x40) // VCOMH deselect level
	oledSendCmd(fd, 0xA4)       // display from RAM
	oledSendCmd(fd, 0xA6)       // normal (not inverted)
	oledSendCmd(fd, 0xAF)       // display on
	time.Sleep(100 * time.Millisecond)
}

// oledInitSH1106 initialises an SH1106 128x64 OLED display.
func oledInitSH1106(fd int) {
	oledSendCmd(fd, 0xAE)       // display off
	oledSendCmd(fd, 0xD5, 0x80) // clock divider
	oledSendCmd(fd, 0xA8, 0x3F) // MUX ratio = 64
	oledSendCmd(fd, 0xD3, 0x00) // display offset = 0
	oledSendCmd(fd, 0x40)       // start line = 0
	oledSendCmd(fd, 0xAD, 0x8B) // DC-DC on (SH1106-specific)
	oledSendCmd(fd, 0xA1)       // segment remap
	oledSendCmd(fd, 0xC8)       // COM scan direction
	oledSendCmd(fd, 0xDA, 0x12) // COM pins config
	oledSendCmd(fd, 0x81, 0xCF) // contrast
	oledSendCmd(fd, 0xD9, 0xF1) // pre-charge period
	oledSendCmd(fd, 0xDB, 0x40) // VCOMH deselect level
	oledSendCmd(fd, 0xA4)       // display from RAM
	oledSendCmd(fd, 0xA6)       // normal
	oledSendCmd(fd, 0xAF)       // display on
	time.Sleep(100 * time.Millisecond)
}

// oledClearSSD1306 clears the entire SSD1306 framebuffer.
func oledClearSSD1306(fd int) {
	oledSendCmd(fd, 0x21, 0, 127) // column range 0-127
	oledSendCmd(fd, 0x22, 0, 7)   // page range 0-7
	zeros := make([]byte, 128)
	for page := 0; page < 8; page++ {
		oledSendData(fd, zeros)
	}
}

// oledClearSH1106 clears the entire SH1106 framebuffer.
func oledClearSH1106(fd int) {
	zeros := make([]byte, 128)
	for page := 0; page < 8; page++ {
		oledSetPosSH1106(fd, page, 0)
		oledSendData(fd, zeros)
	}
}

// oledSetPosSH1106 sets the cursor position on an SH1106 (page addressing mode).
// SH1106 has 132-column RAM; the 128-pixel display is offset by 2.
func oledSetPosSH1106(fd int, page, col int) {
	col += 2 // SH1106 column offset
	oledSendCmd(fd, 0xB0|byte(page))
	oledSendCmd(fd, byte(col&0x0F))
	oledSendCmd(fd, 0x10|byte(col>>4))
}

// oledRenderLine renders a string into a 128-byte page buffer using font5x7,
// centred horizontally. Each character is 6 pixels wide (5 + 1 space).
func oledRenderLine(text string) []byte {
	buf := make([]byte, 128)
	charWidth := 6
	textWidth := len(text) * charWidth
	x := (128 - textWidth) / 2
	if x < 0 {
		x = 0
	}
	for _, ch := range text {
		if x+charWidth > 128 {
			break
		}
		idx := int(ch) - 32
		if idx < 0 || idx >= len(font5x7) {
			idx = 0 // space for unprintable
		}
		for col := 0; col < 5; col++ {
			buf[x+col] = font5x7[idx][col]
		}
		// column 5 is spacing (remains 0)
		x += charWidth
	}
	return buf
}

// oledWriteTextSSD1306 writes centred text on a given page of an SSD1306.
func oledWriteTextSSD1306(fd int, page int, text string) {
	oledSendCmd(fd, 0x21, 0, 127)             // full column range
	oledSendCmd(fd, 0x22, byte(page), byte(page)) // single page
	oledSendData(fd, oledRenderLine(text))
}

// oledWriteTextSH1106 writes centred text on a given page of an SH1106.
func oledWriteTextSH1106(fd int, page int, text string) {
	oledSetPosSH1106(fd, page, 0)
	oledSendData(fd, oledRenderLine(text))
}

// InitOLED finds any detected OLED displays and shows a startup message.
func InitOLED(i2cDevices []DetectedI2CDevice) {
	for _, dev := range i2cDevices {
		if dev.DeviceType != "oled" {
			continue
		}

		busPath := fmt.Sprintf("/dev/i2c-%d", dev.Bus)
		fd, err := unix.Open(busPath, unix.O_RDWR, 0)
		if err != nil {
			slog.Debug("oled init: cannot open bus", "path", busPath, "error", err)
			continue
		}

		addr := uint8(0x3C)
		fmt.Sscanf(dev.Address, "0x%x", &addr)
		if err := unix.IoctlSetInt(fd, i2cSlave, int(addr)); err != nil {
			unix.Close(fd)
			slog.Debug("oled init: cannot set slave", "addr", dev.Address, "error", err)
			continue
		}

		switch dev.OLEDChip {
		case "SSD1306":
			oledInitSSD1306(fd)
			oledClearSSD1306(fd)
			oledWriteTextSSD1306(fd, 2, "OLED Detected")
			oledWriteTextSSD1306(fd, 5, "Pi-Star MCP")
		case "SH1106":
			oledInitSH1106(fd)
			oledClearSH1106(fd)
			oledWriteTextSH1106(fd, 2, "OLED Detected")
			oledWriteTextSH1106(fd, 5, "Pi-Star MCP")
		default:
			// Unknown chip — try SSD1306 as it's most common
			oledInitSSD1306(fd)
			oledClearSSD1306(fd)
			oledWriteTextSSD1306(fd, 2, "OLED Detected")
			oledWriteTextSSD1306(fd, 5, "Pi-Star MCP")
		}

		unix.Close(fd)
		slog.Info("oled startup message displayed", "bus", dev.Bus, "address", dev.Address, "chip", dev.OLEDChip)
	}
}

